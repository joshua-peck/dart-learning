void main() {
	// Data Types
	// Numbers, Strings, Booleans, Lists, Maps, Dynamic

	// Strings
	String firstName = "James";
	print("String: $firstName");

	// Integers
	int answer = 42;
	print("answer: $answer");

	// Doubles
	double pi = 3.14159;
	print("pi: $pi");

	// Booleans
	bool isLogical = false;
	print("isLogical: $isLogical");

	// Dynamic
	dynamic fullName = "James T. Kirk";
	print("Dynamic: $fullName");
}
